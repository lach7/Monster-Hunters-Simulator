-- The pack contains 21 different Themes, 352 assets in total and 823,000 triangles in blender.
-- Feel free to use whatever assets you want for you games but please don't resell or reupload them as your own.


-- INFO
-- The robotic arm in the Sci-Fi pack is rigged and can be animated easily. it comes with several heads which can be attached
-- to the arm by simply welding it to the top part of the arm.

-- The holographic table in the pack also has a rotating hologram.
-- For it to work, the cone part must be anchored and the hologram you want to have must be welded to it, as the cone is the
-- part that spins. The default asset for it is a holographic cargo crate.
-- There is already a script in there that unanchors the default hologram so it spins but it may need to be altered
-- depending on the asset you use.

-- The physics lampposts all have swinging lamps that are joined using a rope contstraint, where as the normal ones are a
-- solid mesh and don't move. There are scripts in the phyics lampposts to unanchor the lamps incase you do it as they can't
-- move if they're anchored.

-- Also, you may notice that i've used a different method for texturing all of the meshes. The separate colours are all coloured
-- in studio instead of using a texture in blender, which i have several reasons for:

-- Because they're coloured in studio, the colours can be changed at any time and means you don't have to create a new
--  texture for each new colour.

-- I don't like the look of the texture grid that everyone uses in blender, as its a strange way to colour it and the gradient looks odd.

